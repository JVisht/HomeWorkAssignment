package math;

public class MakePyramid {

  /*

    Implement a large Pyramid of stars in the screen with java.

          *
         * *
        * * *
       * * * *
      * * * * *
     * * * * * *

     */

    public static void main(String[] args) {

        for (int j = 0; j < 6 - 0; j++) {
            System.out.print(" ");
        }
        for (int k = 0; k <= 0; k++) {
            System.out.print("* ");
        }
        System.out.println();
        for (int j = 0; j < 6 - 1; j++) {
            System.out.print(" ");
        }
        for (int k = 0; k <= 1; k++) {
            System.out.print("* ");
        }
        System.out.println();
        for (int j = 0; j < 6 - 2; j++) {
            System.out.print(" ");
        }
        for (int k = 0; k <= 2; k++) {
            System.out.print("* ");
        }
        System.out.println();
        for (int j = 0; j < 6 - 3; j++) {
            System.out.print(" ");
        }
        for (int k = 0; k <= 3; k++) {
            System.out.print("* ");
        }
        System.out.println();
        for (int j = 0; j < 6 - 4; j++) {
            System.out.print(" ");
        }
        for (int k = 0; k <= 4; k++) {
            System.out.print("* ");
        }
        System.out.println();
        for (int j = 0; j < 6 - 5; j++) {
            System.out.print(" ");
        }
        for (int k = 0; k <= 5; k++) {
            System.out.print("* ");
        }
        System.out.println();
    }

}





